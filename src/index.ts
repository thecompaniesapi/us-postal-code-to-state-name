const data = require('./data.json')

/**************************************************************************
 * DATA
 ***************************************************************************/

export { data }

/**************************************************************************
 * METHODS
 ***************************************************************************/

export const convertPostalCodeToStateName = (postalCode: string): string | null => {
  postalCode = postalCode.toLowerCase()

  if (data[postalCode]) {
    return data[postalCode].name
  }

  return null
}
